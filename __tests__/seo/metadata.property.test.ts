// ============================================================================
// Helper Functions
// ============================================================================

/**
 * Check if metadata has required fields (title and description)
 */
function hasCompleteMetadata(metadata: Metadata | undefined): boolean {
  if (!metadata) return false;
  
  // Check for title
  const hasTitle = typeof metadata.title === 'string' && metadata.title.length > 0;
  
  // Check for description
  const hasDescription = typeof metadata.description === 'string' && metadata.description.length > 0;
  
  return hasTitle && hasDescription;
}

